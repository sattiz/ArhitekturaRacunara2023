# На языке ассемблера реализовать умножение двух чисел с помощью сложения,использование
# переменных
# а. Задача идентична задаче 2 из первого семестра упражнений; единственное отличие состоит в том, что
# умножаемые два числа, а также результат должны быть помещены в переменные (маркированные
# ячейки памяти), а не в регистры; числа можно поместить в переменную сразу при инициализации (в
# части кода .section .data), ее не обязательно использоватьдвигатьсяинструкция
# б. Реализовать оптимизацию как в задаче 2 с первого слагаемого упражнения (минимальное количество
# итераций цикла)


.global _start

    .data
first: .long 156
second: .long 2
result: .long 0

    .text
_start:
    movl (first), %ecx
    movl (second), %ebx
    cmp %ecx, %ebx
    jg multiply
    xchg %ecx, %ebx

    multiply:
        xor %eax, %eax
        begin_loop:
            cmp $0, %ecx
            je end_loop
            decl %ecx
            addl %ebx, %eax
            jmp begin_loop
        end_loop:
    movl %eax, result

    movl $1, %eax
    movl $0, %ebx
    int  $0x80

